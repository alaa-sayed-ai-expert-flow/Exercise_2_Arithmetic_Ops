num1 = float(input("Enter the First Number: "))
num2 = float(input("Enter the Second Number: "))

#addition

print("{} + {} = ".format(num1, num2))
print(num1 + num2)

#subtraction

print("{} - {} = ".format(num1, num2))
print(num1 - num2)

#multiplication

print("{} * {} = ".format(num1, num2))
print(num1 * num2)

#division

print("{} / {} = ".format(num1, num2))
print(num1 / num2)
